# GP-50 PatchToEnglish v5
# Translates GP-50 firmware from Chinese to English
# License: OpenCode

param(
    [Parameter(ValueFromRemainingArguments=$true)]
    [string[]]$Arguments
)

$ScriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$OutputFolder = $ScriptDir

$InputFile = $null
$OutputFile = $null
$ForcePatch = $false
$Region = $null
$ShowHelp = $false
$ShowMan = $false

$i = 0
while ($i -lt $Arguments.Count) {
    $arg = $Arguments[$i]
    switch -Regex ($arg) {
        '^-input$' { $InputFile = $Arguments[++$i] }
        '^-output$' { $OutputFile = $Arguments[++$i] }
        '^-r$' { 
            $Region = @($Arguments[++$i], $Arguments[++$i])
        }
        '^--force-patch$' { $ForcePatch = $true }
        '^--help$' { $ShowHelp = $true }
        '^--man$' { $ShowMan = $true }
        '^-input(.+)' { $InputFile = $Matches[1] }
        '^-output(.+)' { $OutputFile = $Matches[1] }
        '^-r(.+)' { 
            $parts = $Matches[1] -split '\s+'
            $Region = @($parts[0], $parts[1])
        }
        '^--force-patch$' { $ForcePatch = $true }
    }
    $i++
}

